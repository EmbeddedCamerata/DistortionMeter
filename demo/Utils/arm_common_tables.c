/**
 * \par
 * Pseudo code for Generation of Bit reversal Table is
 * \par
 * <pre>for(l=1;l <= N/4;l++)
 * {
 *   for(i=0;i<logN2;i++)
 *   {
 *     a[i]=l&(1<<i);
 *   }
 *   for(j=0; j<logN2; j++)
 *   {
 *     if (a[j]!=0)
 *     y[l]+=(1<<((logN2-1)-j));
 *   }
 *   y[l] = y[l] >> 1;
 *  } </pre>
 * \par
 * where N = 4096	logN2 = 12
 * \par
 * N is the maximum FFT Size supported
 */

